/*
Debemos lograr tomar un nombre con 'prompt' 
y luego mostrarlo por 'alert' al presionar el botón  'mostrar'*/
function mostrar()
{
    let nombre;              // declare la variable nombre 
	nombre = prompt("Ingrese su nombre"); // la variable va a ser igual a lo que se ingrese en el cuadro de texto del prompt
	alert(nombre);       // pedí mostrar la variable en un cuadro emergente
}
